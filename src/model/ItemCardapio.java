package model;

public class ItemCardapio {
	
	private Lanche lanche;
	
	private int valor;

	//construtor da classe:
	public ItemCardapio(int valor) {
		this.lanche = lanche;
	}

	//getters e setters
	public Lanche getLanche() {
		return lanche;
	}

	public void setLanche(Lanche lanche) {
		this.lanche = lanche;
	}

	public int getValor() {
		return valor;
	}

	public void setValor(int valor) {
		this.valor = valor;
	}

	
}
